#!/bin/bash
# ─────────────────────────────────────────────────────────────────────────────
# post-create.sh
# コンテナ初回作成後に実行されるセットアップスクリプト
# ─────────────────────────────────────────────────────────────────────────────

set -e  # エラーで即停止

echo ""
echo "╔══════════════════════════════════════════════════════════╗"
echo "║     Effortless Lite - DevContainer Setup                 ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""

# ─────────────────────────────────────────────────────────────────────────────
# 1. Gradle Wrapper のセットアップ
#    NeoForge MDK の gradlew がなければ Gradle でラッパーを生成する
# ─────────────────────────────────────────────────────────────────────────────
echo ">>> [1/4] Gradle Wrapper のセットアップ..."

if [ ! -f "./gradlew" ]; then
    echo "    gradlew が見つかりません。自動生成します..."
    gradle wrapper --gradle-version 8.8 --distribution-type bin
    echo "    gradlew を生成しました ✓"
else
    echo "    gradlew は既に存在します ✓"
fi

chmod +x ./gradlew 2>/dev/null || true  # Windowsマウント時はエラーを無視

# ─────────────────────────────────────────────────────────────────────────────
# 2. NeoForge MDK の依存関係をダウンロード
#    初回は時間がかかる (NeoForge ライブラリ ~200MB)
# ─────────────────────────────────────────────────────────────────────────────
echo ""
echo ">>> [2/4] NeoForge 依存関係のダウンロード (初回は数分かかります)..."
./gradlew dependencies --no-daemon --quiet 2>&1 | tail -5 || true
echo "    依存関係のダウンロード完了 ✓"

# ─────────────────────────────────────────────────────────────────────────────
# 3. NeoForge の実行環境を準備
#    runData タスクでゲームのリソースを生成する
# ─────────────────────────────────────────────────────────────────────────────
echo ""
echo ">>> [3/4] NeoForge 実行環境の準備..."
./gradlew --no-daemon compileJava --quiet 2>&1 | tail -5 || {
    echo "    ⚠️  コンパイルエラーがあります。VS Code でソースを確認してください。"
}
echo "    実行環境の準備完了 ✓"

# ─────────────────────────────────────────────────────────────────────────────
# 4. Git の設定
# ─────────────────────────────────────────────────────────────────────────────
echo ""
echo ">>> [4/4] Git の初期設定..."
if [ ! -d ".git" ]; then
    git init
    git add .
    git commit -m "feat: Initial commit - Effortless Lite Mod"
    echo "    Git リポジトリを初期化しました ✓"
else
    echo "    Git リポジトリは既に存在します ✓"
fi

# ─────────────────────────────────────────────────────────────────────────────
# 完了メッセージ
# ─────────────────────────────────────────────────────────────────────────────
echo ""
echo "╔══════════════════════════════════════════════════════════╗"
echo "║     セットアップ完了！                                    ║"
echo "╠══════════════════════════════════════════════════════════╣"
echo "║                                                          ║"
echo "║  よく使うコマンド:                                        ║"
echo "║    ./gradlew runClient   → クライアント起動              ║"
echo "║    ./gradlew runServer   → サーバー起動                  ║"
echo "║    ./gradlew build       → JARビルド                     ║"
echo "║    ./gradlew compileJava → コンパイルのみ                 ║"
echo "║                                                          ║"
echo "║  生成されるJAR:                                           ║"
echo "║    build/libs/effortlesslite-1.0.0.jar                   ║"
echo "║                                                          ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""
